#include<stdio.h>

void main(){
    /// Declare Variables
    int i;
    char c;
    float f;
    double d;
    long int li;
    short int si;

    /// Initialize Variable
    i = 3345;
    c = 'c';
    f = 2323.643;
    d = 34543.356563;
    li = 46576;
    si = 254;



    /// Show Variable Values
    printf("%i\n",i);
    printf("%c\n",c);
    printf("%f\n",f);
    printf("%lf\n",d);
    printf("%ld\n",li);
    printf("%hi\n",si);


}
